<script>
    function kiemtra() {
        thanhcong = true;
        thongbao = "";

        //kiem tra ten sp
        var sp = document.getElementById("ten");
        if (sp.value.length == 0) {
            thongbao += "Vui lòng nhập tên<br>";
            thanhcong = false;
        }

        var sp = document.getElementById("email");
        if (sp.value.length == 0) {
            thongbao += "Vui lòng nhập email<br>";
            thanhcong = false;
        }

        var sp = document.getElementById("sdt");
        if (sp.value.length == 0) {
            thongbao += "Vui lòng nhập số điện thoại<br>";
            thanhcong = false;
        }

        var sp = document.getElementById("pw");
        if (sp.value.length == 0) {
            thongbao += "Vui lòng nhập password<br>";
            thanhcong = false;
        }

        if (thongbao == "") thongbao = "Nhập đúng dữ liệu";
        document.getElementById("kq").innerHTML = thongbao;
        return thanhcong;
    }
</script>

